int	ft_iterative_factorial(int nb)
{
	int	res;
	int	n;

	n = 1;
	res = 1;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (nb >= n)
	{
		res *= n;
		n++;
	}
	return (res);
}
